#将数据转换成字符串
num_str = str(132)
print(type(num_str),num_str)

foalt_str = str(13.2)
print(type(foalt_str),foalt_str)
#将字符串转换成数字
num_int = int("132")
print(type(num_int),num_int)

num_float = float("13.2")
print(type(num_float),num_float)
