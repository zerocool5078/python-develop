#单引号定义
name = 'kun'
print(type(name))  # 输出 name 的类型
#双引号定义
name2 = "kun"
print(type(name2))  # 输出 name2 的类型
#三引号定义
name3 = '''
i
am
kun'''
print(type(name3))  # 输出 name3 的类型

#在字符串内包含单引号和双引号
name4 = "I'm kun"
print(name4)  # 输出 name4 的值
name5 = 'He said, "Hello!"'
print(name5)  # 输出 name5 的值
#转义字符
name6 = ' "I\'m kun"'
print(name6)  # 输出 name6 的值
name7 = '\'kun\''
print(name7)  # 输出 name7 的值